package org.operators.java;

public class Operators {

	public static void main(String[] args) {
		int a=10,b=20;
		//arithmetic operators
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a/b);
		System.out.println(a*b);
		//assignment operator
		a+=1;b-=1;
		//increment decrement
		System.out.println(++a);
		System.out.println(++b);
		System.out.println(a++);
		System.out.println(a--);
		System.out.println(b--);
        //
	}

}
